/// 유니코드 코드포인트 하나를 기본 자모 시퀀스로 분해한다(내부용).
List<String> _decomposeCode(int code) {
  if (code < _sBase || code > _sLast) {
    return [String.fromCharCode(code)];
  }
  final sIndex = code - _sBase;
  final cho = sIndex ~/ (_jungCount * _jongCount);
  final jung = (sIndex % (_jungCount * _jongCount)) ~/ _jongCount;
  final jong = sIndex % _jongCount;
  return [
    ..._choseong[cho],
    ..._jungseong[jung],
    ..._jongseong[jong],
  ];
}

/// 한 글자(완성형 한글 음절)를 기본 자모 시퀀스로 분해한다.
/// 완성형 한글이 아니거나(공백/한자/영문 등) 분해 불가 시 해당 문자를 그대로 1개 원소로 반환.
List<String> decomposeChar(String ch) => _decomposeCode(ch.runes.first);

/// 단어(여러 글자) 전체를 기본 자모 시퀀스로 분해한다.
List<String> decomposeWord(String word) {
  final result = <String>[];
  for (final code in word.runes) {
    result.addAll(_decomposeCode(code));
  }
  return result;
}

/// 단어가 이 게임의 후보로 유효한가?
/// - 모든 글자가 완성형 한글이고
/// - 기본 자모 24개로만 분해되며
/// - 분해 결과 길이가 정확히 [length] (기본 5) 이다.
bool isValidCandidate(String word, {int length = 5}) {
  final jamo = decomposeWord(word.trim());
  if (jamo.length != length) return false;
  for (final j in jamo) {
    if (!kBasicJamo.contains(j)) return false;
  }
  return true;
}
