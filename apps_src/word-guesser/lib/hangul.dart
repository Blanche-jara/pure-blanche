/// 중성 21개를 "기본 모음 시퀀스"로 펼친 표 (유니코드 인덱스 순서).
/// 모든 합성 모음을 두벌식 입력대로 기본 모음 10개(ㅏㅑㅓㅕㅗㅛㅜㅠㅡㅣ)로 펼친다.
