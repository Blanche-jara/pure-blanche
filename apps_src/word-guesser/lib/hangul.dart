/// 한글 자모 분해/조합 유틸리티 (카카오톡 "오늘의 단어" 5자모, 꼬들 6자모, 꼬오오오오들 12자모 공용).
///
/// 세 게임 모두 화면 키보드에 **기본 자음 14개 + 기본 모음 10개**(총 24개)만 있고,
/// 그리드의 각 칸은 **기본 자모 하나**에 대응한다(append-only 입력, 두벌식 조합 없음).
/// 따라서 모든 단어를 "기본 자모 24개 시퀀스"로 완전 분해한다. 합성 글자는 두벌식 입력
/// 순서대로 기본 자모로 펼친다:
///   - 쌍자음: ㄲ → ㄱ+ㄱ, ㄸ → ㄷ+ㄷ, ㅃ → ㅂ+ㅂ, ㅆ → ㅅ+ㅅ, ㅉ → ㅈ+ㅈ (초성·받침 모두)
///   - 합성 모음: ㅐ → ㅏ+ㅣ, ㅔ → ㅓ+ㅣ, ㅘ → ㅗ+ㅏ, ㅙ → ㅗ+ㅏ+ㅣ, ㅢ → ㅡ+ㅣ ...
///   - 겹받침: ㄺ → ㄹ+ㄱ, ㅄ → ㅂ+ㅅ ...
/// 결과적으로 모든 완성형 한글은 24개 기본 자모로만 분해되며, 후보 필터는 길이로만 구분한다.
library;

/// 게임 키보드에 실제 존재하는 기본 자음 14개 (그리드 등장 순서와 무관, 사전식 정렬).
const List<String> kBasicConsonants = [
  'ㄱ', 'ㄴ', 'ㄷ', 'ㄹ', 'ㅁ', 'ㅂ', 'ㅅ',
  'ㅇ', 'ㅈ', 'ㅊ', 'ㅋ', 'ㅌ', 'ㅍ', 'ㅎ',
];

/// 게임 키보드에 실제 존재하는 기본 모음 10개.
const List<String> kBasicVowels = [
  'ㅏ', 'ㅑ', 'ㅓ', 'ㅕ', 'ㅗ', 'ㅛ', 'ㅜ', 'ㅠ', 'ㅡ', 'ㅣ',
];

/// 게임에서 사용 가능한 24개 기본 자모 전체.
final Set<String> kBasicJamo = {...kBasicConsonants, ...kBasicVowels};

const int _sBase = 0xAC00; // '가'
const int _sLast = 0xD7A3; // '힣'
const int _jungCount = 21;
const int _jongCount = 28;

/// 초성 19개를 "기본 자음 시퀀스"로 펼친 표 (유니코드 인덱스 순서).
/// 쌍자음은 두벌식 입력대로 같은 자음 두 번으로 펼친다 (ㄲ → ㄱ+ㄱ).
const List<List<String>> _choseong = [
