/// 종성 28개를 "기본 자음 시퀀스"로 펼친 표 (유니코드 인덱스 순서, 0 = 받침 없음).
/// 쌍받침 ㄲ, ㅆ 도 같은 자음 두 번으로, 겹받침은 두 자음으로 펼친다.
